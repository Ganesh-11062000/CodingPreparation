def countBits(num):
	res = [0 for i  in range(num+1)]

	floor = 1
	ceil = 2

	for i in range(1,num+1):
		if i == ceil:
			floor = ceil
			ceil = floor*2

		res[i] = res[i-floor]+1

	return res

print(countBits(16))

